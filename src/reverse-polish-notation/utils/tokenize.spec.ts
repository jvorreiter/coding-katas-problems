import { BinaryOperatorToken, NumberToken, ParenthesisToken } from "../types/Token";
import { tokenize } from "./tokenize";

describe('tokenize', () => {
    it('should tokenize correctly', () => {
        const tokens = tokenize('1 + 2 - 3 * 4 / 5 ^ 6.1 ([{<>}])');

        expect(tokens).toHaveLength(19);

        expect(tokens[0].type).toBe('number');
        expect((tokens[0] as NumberToken).value).toBe(1);

        expect(tokens[1].type).toBe('binary-operator');
        expect((tokens[1] as BinaryOperatorToken).operator).toBe('plus');

        expect(tokens[2].type).toBe('number');
        expect((tokens[2] as NumberToken).value).toBe(2);

        expect(tokens[3].type).toBe('binary-operator');
        expect((tokens[3] as BinaryOperatorToken).operator).toBe('minus');

        expect(tokens[4].type).toBe('number');
        expect((tokens[4] as NumberToken).value).toBe(3);

        expect(tokens[5].type).toBe('binary-operator');
        expect((tokens[5] as BinaryOperatorToken).operator).toBe('multiply');

        expect(tokens[6].type).toBe('number');
        expect((tokens[6] as NumberToken).value).toBe(4);

        expect(tokens[7].type).toBe('binary-operator');
        expect((tokens[7] as BinaryOperatorToken).operator).toBe('divide');

        expect(tokens[8].type).toBe('number');
        expect((tokens[8] as NumberToken).value).toBe(5);

        expect(tokens[9].type).toBe('binary-operator');
        expect((tokens[9] as BinaryOperatorToken).operator).toBe('exponentiate');

        expect(tokens[10].type).toBe('number');
        expect((tokens[10] as NumberToken).value).toBe(6.1);

        for (let i = 0; i < 4; i++) {
            const token = tokens[11 + i];
            expect(token.type).toBe('parenthesis');
            expect((token as ParenthesisToken).open).toBe(true);
        }
        for (let i = 0; i < 4; i++) {
            const token = tokens[15 + i];
            expect(token.type).toBe('parenthesis');
            expect((token as ParenthesisToken).open).toBe(false);
        }
    });
});