package org.inmediasp.codingKatasProblems.reversePolishNotation.utils;

import static org.junit.jupiter.api.Assertions.*;

import org.inmediasp.codingKatasProblems.reversePolishNotation.tokens.*;
import org.junit.jupiter.api.Test;

public class TokenizerTest {
    @Test
    public void tokenizeTest() {
        var tokenizer = new Tokenizer();
        var tokens = tokenizer.tokenize("1 + 2 - 3 * 4 / 5 ^ 6.1 ([{<>}])");

        assertEquals(19, tokens.size());

        assertInstanceOf(NumberToken.class, tokens.get(0));
        assertEquals(1, ((NumberToken) tokens.get(0)).getValue());

        assertInstanceOf(BinaryOperatorToken.class, tokens.get(1));
        assertEquals(BinaryOperator.plus,((BinaryOperatorToken) tokens.get(1)).getOperator());

        assertInstanceOf(NumberToken.class, tokens.get(2));
        assertEquals(2, ((NumberToken) tokens.get(2)).getValue());

        assertInstanceOf(BinaryOperatorToken.class, tokens.get(3));
        assertEquals(BinaryOperator.minus,((BinaryOperatorToken) tokens.get(3)).getOperator());

        assertInstanceOf(NumberToken.class, tokens.get(4));
        assertEquals(3, ((NumberToken) tokens.get(4)).getValue());

        assertInstanceOf(BinaryOperatorToken.class, tokens.get(5));
        assertEquals(BinaryOperator.multiply,((BinaryOperatorToken) tokens.get(5)).getOperator());

        assertInstanceOf(NumberToken.class, tokens.get(6));
        assertEquals(4, ((NumberToken) tokens.get(6)).getValue());

        assertInstanceOf(BinaryOperatorToken.class, tokens.get(7));
        assertEquals(BinaryOperator.divide,((BinaryOperatorToken) tokens.get(7)).getOperator());

        assertInstanceOf(NumberToken.class, tokens.get(8));
        assertEquals(5, ((NumberToken) tokens.get(8)).getValue());

        assertInstanceOf(BinaryOperatorToken.class, tokens.get(9));
        assertEquals(BinaryOperator.exponentiate,((BinaryOperatorToken) tokens.get(9)).getOperator());

        assertInstanceOf(NumberToken.class, tokens.get(10));
        assertEquals(6.1, ((NumberToken) tokens.get(10)).getValue());

        for (int i = 0; i < 4; i++) {
            var token = tokens.get(11 + i);
            assertInstanceOf(ParenthesisToken.class, token);
            assertTrue(((ParenthesisToken) token).isOpen());
        }
        for (int i = 0; i < 4; i++) {
            var token = tokens.get(15 + i);
            assertInstanceOf(ParenthesisToken.class, token);
            assertFalse(((ParenthesisToken) token).isOpen());
        }
    }
}
